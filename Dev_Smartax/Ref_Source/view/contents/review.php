			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				언제부터 ICT를 도입하였는가?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				2009년 경북농민사관학교 경북농업기술원 경영정보대학에서 산천경제연구소 이원노 소장님의 강의를 듣고 
				농업경영을 제대로 하고자 하는 마음으로 도입하게 되었습니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT를 영농현장에 적용한 동기 또는 배경은?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				강의를 들으면서 삼성 이병철 회장, 현대 정주영 회장의 회계와 경영이 투명 하였으므로 오늘날의 현대와 삼성이 
				되었다는 말씀과 투명한 농업경영이 되지 않고는 회계의 명확성이 없기 때문에 반드시 필요하다고 생각 하여 
				영농현장에 적용하기로 했습니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT가 영농현장에 필요한 이유는?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				수기로 판매 장부를 작성할 때는 여러 가지 한계를 많이 느꼈습니다.
				매일 입력은 하지만 유기농의 특성상 단골 고객, 재구매 고객이 많은 관계로 고객이 재주문을 할 경우 지난 번
				주소를 찾기가 쉽지 않았기 때문 이었습니다. 그리고 매월별, 고객매출별, 작목별, 매출액을 파악을 할 수가
				없었습니다.<br>
				그러나 태극회계를 도입한 후부터는 그날 그날 매출액과 지출액을 고단한 몸과 침침한 눈으로 입력을 하기가
				힘이 들지만 단골 고객의 주소는 물론이고 작목별, 고객별, 월별, 매출액을 한 눈에 파악함으로써 그 달의 수익과
				비용을 명확히 파악 될 수 있습니다 뿐만 아니라 올해 최고 소득 품목을 알게 됨으로 내년 농사에 고객의 수요를
				예상하고 준비할 수 있는 자료가 됩니다.
			</p>
