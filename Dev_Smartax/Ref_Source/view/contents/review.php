			<strong class="text-box-03-title">○ ICT 활용시 고려 및 개선사항</strong>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT를 영농현장에 활용 시 고려할 사항은?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				처음부터 투자대비 성공여부를 결정할 수 없다고 생각 합니다.
				왜냐하면 5년동안 혼자서 노력한 결과 단순히 지금의 한빛농장이 있다고 생각한다면 큰 오산이라 생각 합니다.
				부모가 자식을 키우면서 비용을 생각하며 키우는 것이 아니라고 생각 합니다. 살아가면서 모르는 것은 알아야하고
				잘 하지 못하면 잘하는 사람에게서 배워서라도 알아야 한다고 생각 합니다 그러므로 농사는 일년지계요 교육은
				백년대계라는 말이 생각납니다.
				늘 기록하고 메모하고 입력하는 농업인이 되어야 한다는 생각을 합니다.
				좋은 프로그램은 받아들이고 모르면 묻고 배워서 투명한 경영이 되도록 해야 한다고 생각 합니다 할 수 있다는
				마음가짐으로  꾸준히 노력 한다면 영농현장에서 성공적인 경영이 될 것으로 생각 합니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT를 영농현장에 활용시 개선 및 발전방안은?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				늘 농장일을 하면서도 손에는 카메라와 메모지를 준비해야 합니다.
				전화를 받고 메모를 하지 않게 되면 저녁에 와서 누가 무엇을 주문 했는지 고객의 주문 내용 중 빠뜨린 사항이
				있다면 다시 전화를 해야 하므로 시간과 경비가 소요됩니다. 그리고 늘 기록하는 농업인이 되어야 한다는 생각을
				합니다.
				일을 많이 하는 농부가 아니라,
				돈을 잘 버는 농부가 아니라,
				수지 계산을 명확히 하는 경영하는 농업인이
				되어야 한다는 생각을 합니다.
			</p>
