			<p>
				<strong class="color-02">질문</strong><br>
				ICT를 영농현장에 활용한 사례는? (시설관리, 생산관리, 경영관리, 판매관리, 고객관리 등을 구체적으로 기입)
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				경영관리 : 그날 그날 매출과 지출을 입력 하므로 매일, 매주, 매월, 매년 통계뿐만 아니라 작목별 매출액과
				지출액을 알 수 있으므로 순수익을 알 수 있게 되었습니다.<br>
				판매관리 : 당일 매출액을 입력하므로 작목별 매출액이 파악되어 재고량과 장래 수확량을 조절하여 필요 없는
				경비를 지출을 막을 수 있었습니다.<br>
				고객관리 : 고객의 주소, 전화번호 인적 사항뿐만 아니라 구입물품 내역 파악은 물론 구입금액까지 산출 되므로
				매출 순위를 파악할 수 있었습니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT를 활용한 경영개선 및 소득증대 사례는?(경영비 절감, 노동력 절감, 판로개척, 소득증대 등을 구체적으로 기입)
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				전자상거래로 이루어지는 거래이므로 고객의 주소와 인적사항이 무엇보다 중요한데 거래처등록 프로그램에서
				재구매 하시는 고객은 쉽게 파악하여 주문 송장 작성이 매우 용이합니다.
				수기로 일일이 작성할 때는 노력과 시간이 많이 들고 오타로 인한 택배 사고가 한달에 한 두건은  일어나 시간적으로
				물적으로 손해가 많이 있었지만 태극회계프로그램을 이용하여 송장 작성 후에는 택배사고가 거의 없으므로
				비용절감 차원에서 대단히 효과적인 역할을 하게 되었습니다.
				많은 매출이 일어나더라도 기록이 되지 않는다면 명확한 경제와 재정 상태가 파악 되지 않지만 많든 작든 일어난
				매출에 대해서는 명확한 기록과 통계로 농가경영이 확실하게 되었습니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT를 활용하는 나만의 노하우나 기술은?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				처음 태극회계를 도입할 때만 하더라도 남편의 직장이 있었기 때문에 농업경제의 중요성을 인식 하지 못하고
				바쁘다는 핑계로 대충 입력을 하고 년 매출액을 알 수 있는 정도였습니다. 처음 몇 해는 분개장만 입력하고
				거래처등록은 하지도 않았습니다.
				그러나 재구매 반복구매 고객이 많이 생기므로 거래처등록을 하게 되었고
				5년이 지난 지금 너무나 잘 한 선택이었다는 생각이 듭니다. 특별한 노하우나 기술이 아니라 어렵다고 중도에서
				포기하지 않고 꾸준히 노력한 것이 지금의 한빛농장 경영을 눈에 보이도록 투명하게 운영할 수 있었습니다.<br>
				옛말에 버는 자랑 하지 말고 쓰는 곳에 잘 쓰라고 했지만 쓰기 전에 나의 수지. 경영 상태를 파악할 수 있다는 것은
				명확한 농업경영이 된다는 생각을 합니다.
				처음에는  우체국 e-post 에 영덕군 영해면 전체적으로 입력을 하여 한빛농장 개별 통계는 어려웠지만  올해
				3월 4일 부터는  저의 아이디와 비번으로 들어가서 입력하는 미니우체국이 있답니다.
				올해부터 3월 4일 부터 입력된 한빛농장 전체적인 택배 물량을 정확히 산출하고 통계가 가능하게 되었습니다.
			</p>
			<p class="line-dash">
				<strong class="color-02">질문</strong><br>
				ICT 활용의 문제점 및 애로사항은?
			</p>
			<p>
				<strong class="color-03">답변</strong><br>
				2009년 처음 태극회계를 배울 때는 쉬울 것 같았는데 막상 입력을 하다 보니 생각만큼은 쉽지 않았습니다.
				수시로 이원노 소장님을 성가스럽게 전화를 해서 여쭙고 물었으며 정말 배우고 싶고 알고 싶었을 때 2012년
				12월 14일 충청북도 농업기술원까지 혼자서 12인승 스타렉스를 가지고 눈길을 헤치고 다녀오면서 태극회계를
				제대로 하고자 다짐을 했습니다.
				매일 일기를 쓰듯 그 날 수지 상태를 입력해야 한다는 것은 엄청 어려운 일입니다. 그러나 습관과 노력이
				투입된다면 일상의 생활이 된다고 생각 합니다. 처음 태극회계에 고객 연락처를 입력하고 매출을 입력할 때
				남편은 돈만 많이 벌면 되지 그 돈이 그 돈이지 주머니 돈이나 쌈지 돈이나 바쁜 데 쓸데없는 짓을 한다는 핀잔도
				들었습니다.
				가르쳐주신 이원노 소장님마저도 제가 너무 바쁘고 힘들어 하는 것을 아시고는 알바를 쓰든 지 나중에 경리가
				있을 때 사용하라고 하셨지만 시간이 없으면 내가 잠자는 시간을 줄여서라도 직접 입력해야 한다는 생각으로
				전산에 입력을 하지 못하는 날은 그날 지출은 일기장에라도 기록을 하고 시간을 내어 통장과 대조하여 입력을
				하게 되었습니다.
			</p>
